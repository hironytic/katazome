/**
 * The source code of the Katazome runtime file (ktzm-runtime.ts).
 *
 * This string is written to disk when `ktzm transpile` is run (for manual debugging),
 * and to a temporary location when `ktzm generate` is run.
 *
 * The runtime:
 *   - Reads input data from the file path given by KTZM_INPUT_FILE (as JSON).
 *   - Exposes a `ktzm` default export with `out()` and `input`.
 *   - On process exit, writes all accumulated output to KTZM_OUTPUT_FILE,
 *     or to stdout if the env var is not set.
 */
export const runtimeContent = `\
// ktzm-runtime.ts
// Runtime library for Katazome transpiled templates.
// Do not edit manually — this file is generated by \`ktzm transpile\`.
import { readFileSync, writeFileSync } from "node:fs";

const inputFilePath = process.env["KTZM_INPUT_FILE"];
const outputFilePath = process.env["KTZM_OUTPUT_FILE"];

const inputData: unknown = inputFilePath
  ? JSON.parse(readFileSync(inputFilePath, "utf-8"))
  : {};

const chunks: string[] = [];

const ktzm = {
  out(s: string): void {
    chunks.push(s);
  },
  get input(): unknown {
    return inputData;
  },
};

let outputWritten = false;
function writeOutput(): void {
  if (outputWritten) return;
  outputWritten = true;
  const content = chunks.join("");
  if (outputFilePath) {
    writeFileSync(outputFilePath, content);
  } else {
    process.stdout.write(content);
  }
}
process.on("beforeExit", writeOutput);
process.on("exit", writeOutput);

export default ktzm;
`;
