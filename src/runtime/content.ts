/**
 * Describes how the output target is embedded into the generated runtime.
 *
 * - "stdout": output goes to stdout (used by `ktzm transpile` for manual bun run).
 *   KTZM_OUTPUT_FILE_PATH sets the initial value of ktzm.outputFilePath.
 * - "file": a fixed output file path is embedded as a literal. ktzm.outputFilePath
 *   is readable and writable but changes are not reflected in the actual output path.
 * - "directory": an output directory and an initial relative path are embedded.
 *   ktzm.outputFilePath controls the actual output file within that directory.
 *   Paths that resolve outside the directory are rejected with an error.
 *   existingFileBehavior controls what happens when the output file already exists.
 * - "directory-prompt": like "directory" but the runtime writes to temporary files
 *   instead of the final destination; the renderer handles the prompt and file copy.
 */
export type OutputEmbed =
  | { kind: "stdout" }
  | { kind: "file"; filePath: string; initialRelativePath: string }
  | { kind: "directory"; outputDir: string; initialRelativePath: string; existingFileBehavior: "overwrite" | "skip" | "error" }
  | { kind: "directory-prompt"; outputDir: string; contentTmpPath: string; pathTmpPath: string; initialRelativePath: string };

/**
 * Generates the source code of the Katazome runtime file (ktzm-runtime.mts).
 *
 * The runtime exports runKatazome(block), which the transpilate calls with a
 * callback containing the template code. runKatazome handles output and errors.
 *
 * @param inputData   Parsed input data to embed as a JSON literal.
 * @param answerData  Resolved question answers to embed as a JSON literal.
 * @param outputEmbed Describes how output is directed (see OutputEmbed).
 */
export function generateRuntimeContent(
  inputData: unknown,
  answerData: unknown,
  outputEmbed: OutputEmbed,
): string {
  const inputJson = JSON.stringify(inputData ?? {});
  const answerJson = JSON.stringify(answerData ?? {});

  if (outputEmbed.kind === "stdout") {
    return generateStdoutModeRuntime(inputJson, answerJson);
  } else if (outputEmbed.kind === "file") {
    return generateFileModeRuntime(inputJson, answerJson, outputEmbed.filePath, outputEmbed.initialRelativePath);
  } else if (outputEmbed.kind === "directory") {
    return generateDirectoryModeRuntime(inputJson, answerJson, outputEmbed.outputDir, outputEmbed.initialRelativePath, outputEmbed.existingFileBehavior);
  } else {
    return generateDirectoryPromptModeRuntime(inputJson, answerJson, outputEmbed.outputDir, outputEmbed.contentTmpPath, outputEmbed.pathTmpPath, outputEmbed.initialRelativePath);
  }
}

function generatePreamble(inputJson: string, answerJson: string, initialRelativePath: string): string {
  return `\
const inputData: unknown = ${inputJson};

const answerData: unknown = ${answerJson};

const chunks: string[] = [];

let _outputRelativePath: string = ${JSON.stringify(initialRelativePath)};

const _ktzm = {
  out(s: string): void {
    chunks.push(s);
  },
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  get input(): any {
    return inputData as any;
  },
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  get answer(): any {
    return answerData as any;
  },
  get outputFilePath(): string {
    return _outputRelativePath;
  },
  set outputFilePath(value: string) {
    _outputRelativePath = value;
  },
};

let _ranKatazome = false;
`;
}

function generateStdoutModeRuntime(inputJson: string, answerJson: string): string {
  const preamble = generatePreamble(inputJson, answerJson, "");
  return `\
// ktzm-runtime.mts
// Runtime library for Katazome transpiled templates.
// Do not edit manually — this file is generated by \`ktzm transpile\`.

${preamble.trimStart()}
export async function runKatazome(block: (ktzm: typeof _ktzm) => void | Promise<void>): Promise<void> {
  if (_ranKatazome) throw new Error("runKatazome cannot be called more than once");
  _ranKatazome = true;
  _outputRelativePath = process.env["KTZM_OUTPUT_FILE_PATH"] ?? "";
  await block(_ktzm);
  process.stdout.write(chunks.join(""));
}
`;
}

function generateFileModeRuntime(
  inputJson: string,
  answerJson: string,
  filePath: string,
  initialRelativePath: string,
): string {
  const preamble = generatePreamble(inputJson, answerJson, initialRelativePath);
  return `\
// ktzm-runtime.mts
// Runtime library for Katazome transpiled templates.
// Do not edit manually — this file is generated by \`ktzm transpile\`.
import { writeFileSync } from "node:fs";

const _outputFileAbs: string = ${JSON.stringify(filePath)};

${preamble}
export async function runKatazome(block: (ktzm: typeof _ktzm) => void | Promise<void>): Promise<void> {
  if (_ranKatazome) throw new Error("runKatazome cannot be called more than once");
  _ranKatazome = true;
  await block(_ktzm);
  writeFileSync(_outputFileAbs, chunks.join(""));
}
`;
}

function generateDirectoryModeRuntime(
  inputJson: string,
  answerJson: string,
  outputDir: string,
  initialRelativePath: string,
  existingFileBehavior: "overwrite" | "skip" | "error",
): string {
  const preamble = generatePreamble(inputJson, answerJson, initialRelativePath);
  return `\
// ktzm-runtime.mts
// Runtime library for Katazome transpiled templates.
// Do not edit manually — this file is generated by \`ktzm transpile\`.
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { resolve, dirname, sep } from "node:path";

const _outputDirAbs: string = ${JSON.stringify(outputDir)};
const _existingFileBehavior: string = ${JSON.stringify(existingFileBehavior)};

${preamble}
export async function runKatazome(block: (ktzm: typeof _ktzm) => void | Promise<void>): Promise<void> {
  if (_ranKatazome) throw new Error("runKatazome cannot be called more than once");
  _ranKatazome = true;
  await block(_ktzm);
  const resolvedPath = resolve(_outputDirAbs, _outputRelativePath);
  if (!resolvedPath.startsWith(_outputDirAbs + sep) && resolvedPath !== _outputDirAbs) {
    throw new Error(\`ktzm.outputFilePath resolves outside the output directory: "\${_outputRelativePath}"\`);
  }
  if (existsSync(resolvedPath)) {
    if (_existingFileBehavior === "skip") return;
    if (_existingFileBehavior === "error") {
      throw new Error(\`Output file already exists: "\${resolvedPath}". Use a different existingFile setting to allow overwriting or skipping.\`);
    }
  }
  mkdirSync(dirname(resolvedPath), { recursive: true });
  writeFileSync(resolvedPath, chunks.join(""));
}
`;
}

function generateDirectoryPromptModeRuntime(
  inputJson: string,
  answerJson: string,
  outputDir: string,
  contentTmpPath: string,
  pathTmpPath: string,
  initialRelativePath: string,
): string {
  const preamble = generatePreamble(inputJson, answerJson, initialRelativePath);
  return `\
// ktzm-runtime.mts
// Runtime library for Katazome transpiled templates.
// Do not edit manually — this file is generated by \`ktzm transpile\`.
import { writeFileSync } from "node:fs";
import { resolve, sep } from "node:path";

const _outputDirAbs: string = ${JSON.stringify(outputDir)};
const _contentTmpPath: string = ${JSON.stringify(contentTmpPath)};
const _pathTmpPath: string = ${JSON.stringify(pathTmpPath)};

${preamble}
export async function runKatazome(block: (ktzm: typeof _ktzm) => void | Promise<void>): Promise<void> {
  if (_ranKatazome) throw new Error("runKatazome cannot be called more than once");
  _ranKatazome = true;
  await block(_ktzm);
  const resolvedPath = resolve(_outputDirAbs, _outputRelativePath);
  if (!resolvedPath.startsWith(_outputDirAbs + sep) && resolvedPath !== _outputDirAbs) {
    throw new Error(\`ktzm.outputFilePath resolves outside the output directory: "\${_outputRelativePath}"\`);
  }
  writeFileSync(_contentTmpPath, chunks.join(""));
  writeFileSync(_pathTmpPath, _outputRelativePath);
}
`;
}
